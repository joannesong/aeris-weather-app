package com.joannesong.c4q.aeris_weather_app.model;


public class Periods {
    private String icon, validTime;
    private int minTempF, minTempC, maxTempF, maxTempC;

    public String getIcon() {
        return icon;
    }

    public String getValidTime() {
        return validTime;
    }

    public int getMinTempF() {
        return minTempF;
    }

    public int getMinTempC() {
        return minTempC;
    }

    public int getMaxTempF() {
        return maxTempF;
    }

    public int getMaxTempC() {
        return maxTempC;
    }
}
